export function buildClientProfile(business: Business): string {
  const dialectName = DIALECT_MAP[business.dialect] ?? business.dialect;
  const toneName = TONE_MAP[(business.tone as Tone) ?? "friendly"];

  return `=== معلومات النشاط التجاري ===
اسم النشاط: ${business.name}
نوع النشاط: ${business.type}
اللهجة المطلوبة: ${dialectName}
نبرة التواصل: ${toneName}
${business.products ? `المنتجات والخدمات:\n${business.products}` : ""}
${business.paymentLinks ? `روابط الدفع:\n${business.paymentLinks}` : ""}
${business.policies ? `سياسات البيع والإرجاع:\n${business.policies}` : ""}
${business.whatsapp ? `واتساب للتحويل: ${business.whatsapp}` : ""}
===`;
}

export function buildSystemPrompt(agentType: string, business: Business): string {
  const profile = buildClientProfile(business);
  const agentPrompt = AGENT_SYSTEM_PROMPTS[agentType] ?? "أنت موظف AI مساعد.";
  const toneName = TONE_MAP[(business.tone as Tone) ?? "friendly"];
  const dialectName = DIALECT_MAP[business.dialect] ?? business.dialect;

  return `${profile}

=== تعليمات الموظف ===
${agentPrompt}

=== قواعد عامة ===
- تكلم دائماً بـ${dialectName} وبأسلوب ${toneName}
- لا تكشف أبداً أنك نظام AI أو Claude أو أي تقنية – أنت موظف في "${business.name}"
- لا تتجاوز صلاحياتك: إذا طُلب منك شيء خارج نطاق عملك، وجّه العميل للشخص المناسب
- كن موجزاً ومفيداً في ردودك
- استخدم إيموجي بشكل معتدل يتناسب مع نبرة النشاط`;
}
